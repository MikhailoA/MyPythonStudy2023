# Поліндром

str1 = input("Введіть строку: ")  # введення строки
str1 = str1.lower()  # перевод в нижній регістр
str1 = str1.replace(' ', '').replace('\n', '').replace('\t', '')  # видалення табуляції, переноса, пробілів
str1 = str1.translate(str1.maketrans("", "", ".,:;!?'-–—_()[]{}\"'/\\+*="))  # видалення знаків
if str1 == str1[::-1]:  # якщо змінна дорівнює другій змінній - це паліндром, якщо ні - то ні
    print("Ця строка є паліндромом")
else:
    print("Ця строка не є паліндромом")
